let number = Number(prompt("Ingrese un numero del 1 al 3"))
 
switch (number) {
    case 1:
        console.log("Hola bienvenido")
        break;
    case 2:
        console.log("13/5/2026")
        break;
    case 3:
        console.log("Has salido del menu")
        break;
 
    default:
        console.log("Numero invaldo")
        break;
}